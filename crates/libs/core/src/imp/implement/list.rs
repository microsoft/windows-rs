//! `InterfaceList` (sealed) + `Implements<I>` membership marker + tuple impls.
//!
//! See `docs/option-d.md` ("`InterfaceList` and `Implements<I>`").
//!
//! ## Slot indexing
//!
//! Slot indices are pointer-units offsets from the **identity** field
//! (`&'static IInspectable_Vtbl`), not from the start of the struct. Slot 0
//! is identity; declared interfaces start at slot 1. The macro's
//! `OFFSET = -1` for identity and `OFFSET = -2 - k` for chain `k` is the
//! negation of "slot index + one" (the `+1` covers the leading
//! `base: ComposeBase` field).
//!
//! ## Arity
//!
//! Tuple impls cover arity 0..=16 (resolution of OQ-1). Anything larger
//! requires the explicit `VCons` form, which is unsugared but supported.

use super::sealed::Sealed;
use super::storage::{VCons, VNil};
use super::vtbl::VtableCtor;
use crate::{Interface, GUID};

/// Describes the COM interface list a `#[implement]` type exposes.
pub trait InterfaceList: Sealed + 'static {
    /// Number of interface chains, excluding the identity (IInspectable) slot.
    const LEN: usize;

    /// Storage tower (a `VCons` / `VNil` chain).
    type Storage: 'static;

    /// `(IID, slot)` pairs used by the generic `QueryInterface`. Slot 0 is
    /// the identity; declared interfaces start at slot 1.
    const IID_SLOTS: &'static [(GUID, usize)];
}

/// Per-list, per-implementer storage construction. Split from
/// `InterfaceList` because building `Self::Storage` needs to thread `T`
/// through each cell's `VtableCtor<T, OFFSET>::NEW_REF`.
pub trait ListVtables<T: 'static>: InterfaceList {
    const STORAGE: Self::Storage;
}

/// Membership marker — `L: Implements<I>` means "interface `I` appears at
/// position `SLOT` in list `L`". Bounds the blanket `From<T> for I` and
/// `ComObjectInterface<I> for Outer<T, L>` impls in `runtime`.
pub trait Implements<I: Interface>: InterfaceList {
    /// Pointer-units offset from `&self.identity` to the matching vtable
    /// field. 0 = identity, 1 = first declared, etc.
    const SLOT: usize;
}

// =============================================================================
// Tuple impls — arity 0..=16
// =============================================================================
//
// Each tuple impl below is generated by the same shape as today's
// per-`#[implement]` emission, but the work is done once here instead of
// once per use site. The OFFSETs match the macro's `-2 - k` convention so
// the in-memory layout is byte-identical.
//
// `Implements<Ik>` is provided only at the head position of each tuple to
// avoid overlap (`Implements<I0>` and `Implements<I1>` for `(I0, I1)` would
// collide when `I0 == I1`). Production users typically don't need
// `Implements<Ik>` for k > 0 because the proc macro picks the head
// interface for each `From` / `as_interface` call site; the
// hand-written-foundation path is documented to use the explicit `VCons`
// form when richer membership is required.

macro_rules! impl_tuple {
    (
        ( $($I:ident),* )
        storage = $storage:ty,
        $( ($idx:expr, $offs:expr) => $cell:ident ),*
        ;
        head = $head:ident
    ) => {
        impl< $($I: Interface + 'static),* > Sealed for ( $($I,)* ) {}

        impl< $($I: Interface + 'static),* > InterfaceList for ( $($I,)* ) {
            const LEN: usize = impl_tuple!(@count $($I)*);
            type Storage = $storage;
            const IID_SLOTS: &'static [(GUID, usize)] =
                &[ $( ($cell::IID, $idx) ),* ];
        }

        impl<T: 'static, $($I: Interface + 'static),* > ListVtables<T> for ( $($I,)* )
        where
            $( $I::Vtable: VtableCtor<T, $offs> ),*
        {
            const STORAGE: Self::Storage = impl_tuple!(@build $( ($offs, $cell) )*);
        }

        impl< $($I: Interface + 'static),* > Implements<$head> for ( $($I,)* ) {
            const SLOT: usize = 1;
        }
    };

    (@count) => { 0usize };
    (@count $head:ident $($rest:ident)*) => { 1usize + impl_tuple!(@count $($rest)*) };

    (@build) => { VNil };
    (@build ($offs:expr, $cell:ident) $($rest:tt)*) => {
        VCons {
            vtable: <$cell::Vtable as VtableCtor<T, $offs>>::NEW_REF,
            rest: impl_tuple!(@build $($rest)*),
        }
    };
}

// ---- arity 0 -------------------------------------------------------------

impl Sealed for () {}
impl InterfaceList for () {
    const LEN: usize = 0;
    type Storage = VNil;
    const IID_SLOTS: &'static [(GUID, usize)] = &[];
}
impl<T: 'static> ListVtables<T> for () {
    const STORAGE: Self::Storage = VNil;
}

