fn main() {
    windows_bindgen::bindgen([
        "--out",
        "src/bindings.rs",
        "--flat",
        "--sys",
        "--no-deps",
        "--filter",
        "Windows.Win32.System.Services.SERVICE_ACCEPT_TIMECHANGE",
        "Windows.Win32.System.Services.SERVICE_CONTROL_TIMECHANGE",
        "Windows.Win32.System.Services.SERVICE_TIMECHANGE_INFO",
        "Windows.Win32.System.Time.FileTimeToSystemTime",
        "Windows.Win32.Storage.FileSystem.FileTimeToLocalFileTime",
        "--derive",
        "SYSTEMTIME=Debug",
        "SERVICE_TIMECHANGE_INFO=Debug",
    ])
    .unwrap();
}
