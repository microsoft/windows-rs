use windows_clang::nuget_package;
use windows_metadata::merge;

/// The committed, canonical WinRT winmd. Checked in as `windows-bindgen`'s default WinRT
/// metadata (selected with `--in default`), so downstream filters resolve against a stable
/// in-repo winmd without re-running this tool. Re-derived from the pinned SDK Contracts
/// package on every run; treat it as generated output.
const WINMD: &str = "crates/libs/bindgen/default/Windows.winmd";

/// Where the WinRT RDL snapshot is written (committed, human-reviewable), one file per
/// namespace (`Windows.Foundation.rdl`, `Windows.Globalization.rdl`, …). Like `metadata/win32`
/// and `metadata/wdk` this is the reviewable source of truth: the winmd above is compiled from
/// it, so a WinRT metadata change is a readable RDL `git diff`. Regenerated by running this
/// tool; treat it as generated output, not a place to hand-edit.
const RDL_DIR: &str = "metadata/winrt";

/// Where the intermediate merged winmd is written before it is decompiled to RDL. Under
/// `target` and not tracked — regenerated on demand.
const OUT_DIR: &str = "target/winrt";

/// The NuGet package that ships the per-contract WinRT `.winmd` files. Fetched into the
/// NuGet global cache on first use, exactly like `tool_win32`'s SDK packages.
const CONTRACTS_ID: &str = "microsoft.windows.sdk.contracts";

/// Pinned SDK Contracts version. The winmd is merged from this exact package so the WinRT
/// surface is reproducible on any machine and in CI. Bumping it is a deliberate, reviewable
/// change: the new package is restored and `metadata/winrt` + `Windows.winmd` regenerated.
const CONTRACTS_VERSION: &str = "10.0.28000.2270";

/// The per-contract winmds live under this subtree in the package (`Windows.Foundation.winmd`,
/// `Windows.Globalization.winmd`, …), one file per API contract.
const CONTRACTS_SUBDIR: &str = "ref/netstandard2.0";

fn main() {
    let time = std::time::Instant::now();

    // Fetch the pinned Contracts package on demand (shared NuGet cache with the other tools)
    // so a fresh checkout regenerates without a manual `nuget restore`.
    let contracts_dir = nuget_package(CONTRACTS_ID, CONTRACTS_VERSION).join(CONTRACTS_SUBDIR);

    let mut inputs: Vec<String> = std::fs::read_dir(&contracts_dir)
        .unwrap_or_else(|e| panic!("failed to read `{}`: {e}", contracts_dir.display()))
        .filter_map(|entry| entry.ok())
        .map(|entry| entry.path())
        .filter(|path| path.extension().is_some_and(|x| x == "winmd"))
        .map(|path| path.to_string_lossy().replace('\\', "/"))
        .collect();
    inputs.sort();

    assert!(
        !inputs.is_empty(),
        "no `.winmd` files found under `{}`",
        contracts_dir.display()
    );

    // Merge the per-contract winmds into one intermediate winmd, replacing the external
    // `mdmerge` tool with `windows-metadata`'s in-house merger (the same one `tool_win32`/
    // `tool_wdk` use). The per-contract WinRT runtime-class methods and Property/Event tables
    // are dropped by the merge; `windows-bindgen` reconstructs properties/events from the
    // surviving `get_`/`put_`/`add_`/`remove_` accessor methods on the interfaces and never
    // reads class methods.
    std::fs::create_dir_all(OUT_DIR)
        .unwrap_or_else(|e| panic!("failed to create `{OUT_DIR}`: {e}"));
    let merged = format!("{OUT_DIR}/Windows.merged.winmd");
    merge()
        .inputs(&inputs)
        .output(&merged)
        .merge()
        .unwrap_or_else(|e| panic!("failed to merge WinRT contracts into `{merged}`: {e}"));

    // Decompile the merged winmd into the committed per-namespace RDL snapshot, then compile
    // that snapshot back into the canonical `Windows.winmd`. This makes the RDL the source of
    // truth — consistent with `tool_win32`/`tool_wdk`, which likewise scrape to RDL and compile
    // the winmd from it — and lets the `gen` `git diff` validate both the RDL and the winmd. The
    // WinRT surface is self-contained (`System.*` primitives are resolved by the reader), so the
    // reader needs no additional reference winmds; the `Win32`/`Wdk` exclusions guard against any
    // stray references the merge might carry.
    windows_rdl::writer()
        .input(&merged)
        .filters(["Windows", "!Windows.Win32", "!Windows.Wdk"])
        .split(true)
        .output(RDL_DIR)
        .write()
        .unwrap_or_else(|e| panic!("failed to write WinRT RDL to `{RDL_DIR}`: {e}"));

    windows_rdl::reader()
        .input(RDL_DIR)
        .output(WINMD)
        .write()
        .unwrap_or_else(|e| panic!("failed to compile `{WINMD}` from `{RDL_DIR}`: {e}"));

    println!(
        "generated `{RDL_DIR}` and `{WINMD}` from {} contract winmd(s) in {:.2}s",
        inputs.len(),
        time.elapsed().as_secs_f32()
    );
}
