use serde::Deserialize;
use std::path::Path;
use windows_clang::*;
use windows_scraper::{Arch, Config};

/// Where intermediate binary winmd artifacts (per-arch throwaways and the x64
/// scrape that feeds arch-merge) are written. This is under `target` and not
/// tracked — these are regenerated on demand.
const OUT_DIR: &str = "target/win32";

/// The committed, canonical merged winmd. Unlike the per-arch intermediates this
/// is checked in as `windows-bindgen`'s default Win32 metadata, so downstream
/// `tool_bindings` filters can point `--in` at a stable in-repo winmd (and the
/// bundled `"default"` bindings resolve against it) without re-running this tool.
/// Re-derived from the `metadata/win32` RDL corpus (the source of truth) on every
/// run; treat it as generated output.
const WINMD: &str = "crates/libs/bindgen/default/Windows.Win32.winmd";

/// Where the per-header RDL snapshot is written. Unlike the binary winmd, the
/// `.rdl` text is committed to the repository so the canonical Windows API surface
/// can be browsed and reviewed (diffed in pull requests) directly. Regenerated by
/// running this tool; treat it as generated output, not a place to hand-edit.
const RDL_DIR: &str = "metadata/win32";

/// Hand-authored metadata vocabulary seed file (not generated from headers). Defines
/// the `Windows.Win32.Metadata` attribute types the converter emits.
const METADATA_SEED: &str = "metadata.rdl";

/// Pinned Windows SDK version. The corpus is generated against the
/// `Microsoft.Windows.SDK.CPP` (headers) and per-arch `Microsoft.Windows.SDK.CPP.<arch>`
/// (import libs) NuGet packages at this exact version, restored into the NuGet global
/// cache — NOT whatever SDK happens to be installed under `C:\Program Files`. This is
/// what makes the scrape reproducible on any machine and in CI: same headers in, same
/// `.rdl` out. Servicing builds of the same marketing version (`10.0.26100.0`) differ in
/// content, so pinning the package version is the only way to defeat that drift. Bumping
/// it is a deliberate, reviewable change: restore the new packages and regenerate.
const SDK_VERSION: &str = "10.0.28000.2270";

/// The marketing SDK folder nested inside the NuGet package's `c/Include` tree.
const SDK_INCLUDE_DIR: &str = "10.0.28000.0";

// libclang provisioning (pinned version, wheel URLs, resource-header component) lives in
// `windows-clang` so every clang consumer shares one download cache; see
// `windows_clang::ensure_libclang` / `windows_clang::clang_resource_dir`.

/// Clang arguments: parse as C++ so the SDK headers' `extern "C"` blocks,
/// `__declspec`, and SAL annotations are all understood. The target triple is set
/// separately per architecture; the SDK include directories are added as `-isystem`.
const CLANG_ARGS: [&str; 2] = ["-x", "c++"];

/// SAL capture shim, force-included (`-include`) ahead of the translation unit.
/// Under a clang `*-windows-msvc` target the SDK's `<sal.h>` expands SAL to forms
/// libclang drops, so SAL is otherwise invisible; this shim trips the real
/// `<sal.h>` guard and then redefines the high-value macros as `annotate` stubs the
/// scraper can read. See the file header in `sal.h` for details.
const SAL_SHIM: &str = "crates/tools/win32/src/sal.h";

/// A shadow include directory searched (via `-I`) ahead of the SDK `-isystem`
/// directories. It holds hand-authored replacements for SDK headers that cannot
/// be parsed under this scrape's C++ definition mode because they `#include` the
/// C++/WinRT projection headers. Each shim declares only the flat COM/C interop
/// interfaces (which depend on `windows.h` types alone), so the interfaces are
/// scraped without dragging in the projection. Currently:
/// `Windows.Graphics.Capture.Interop.h` (the projection includes trip a
/// self-conflicting typedef in `Windows.Devices.Sensors.h`).
const SHIM_DIR: &str = "crates/tools/win32/src/shims";

/// Prelude included ahead of every API header. `winsock2.h` precedes `windows.h` so
/// its include guard suppresses the legacy `winsock.h` that `windows.h` would otherwise
/// pull (avoiding the `sockaddr` redefinition); most SDK headers then assume the
/// fundamental types (`HANDLE`, `DWORD`, `HWND`, …) are in scope via `windows.h`.
/// `SECURITY_WIN32` selects the user-mode SSPI surface so `sspi.h` (and the headers
/// that include it) compile rather than `#error`-ing on an undefined security package.
const PRELUDE: &str = "#define SECURITY_WIN32\n#include <winsock2.h>\n#include <windows.h>";

/// Reset emitted after the prelude and after every header *in a satellite translation
/// unit* (see [`Manifest::satellite_headers`]) to keep `DEFINE_GUID` in its declaration
/// form.
///
/// Device-driver headers (`ntddstor.h`, `poclass.h`, …) place their `DEFINE_GUID` blocks
/// *outside* their include guards, so a header pulled in more than once re-runs the block.
/// In `INITGUID` *definition* mode that second pass is a `__declspec(selectany)`
/// re-definition that clang rejects — which is why these headers cannot join the main
/// translation unit (where `winusb.h` → `initguid.h` leaves `INITGUID` defined). Parsed in
/// their own satellite TU with this reset after every include, `INITGUID` stays undefined
/// and `guiddef.h` re-derives `DEFINE_GUID` as a plain `extern const GUID` declaration that
/// is harmless to repeat. The faithful GUID values are read from the `DEFINE_GUID` macro
/// arguments, so the declaration/definition distinction does not affect the captured
/// metadata. (The main TU keeps definition mode so its wrapper-macro GUIDs — `vfw.h`'s
/// `DEFINE_AVIGUID`, … — whose values live only in the expanded initializer are preserved.)
const GUID_RESET: &str = "\n#undef INITGUID\n#include <guiddef.h>";

/// The orchestration manifest: a small, mechanical description of which SDK headers
/// to emit (partitioned by their defining header) and the import libraries that
/// record which DLL exports each function. See `win32.toml` for the documented
/// format.
#[derive(Deserialize)]
#[serde(rename_all = "kebab-case", deny_unknown_fields)]
struct Manifest {
    /// Root namespace; each emitted header becomes `<root>.<HeaderStem>`.
    root: String,
    /// API header file names (resolved against the pinned Windows SDK include tree) that
    /// are `#include`d to bring the desired API surface into the translation unit, e.g.
    /// `wingdi.h`. Every *defining header* reachable from these (and the `windows.h`
    /// prelude) is emitted as its own partition — there is no separate list of
    /// foundational headers to maintain, and no allowlist: the closure is automatic.
    headers: Vec<String>,
    /// Headers parsed in their own *satellite* translation unit instead of the main one.
    /// Reserved for headers that cannot co-exist in the main TU — the device-driver
    /// families (`ntddstor.h`, `usbiodef.h`, …) whose `DEFINE_GUID` blocks sit outside
    /// their include guards and collide under definition mode. Parsed in isolation (with
    /// `INITGUID` held off) they emit cleanly; every shared declaration is deduplicated
    /// against the main TU by clang USR, so only their own device partitions are added.
    #[serde(default)]
    satellite_headers: Vec<String>,
    /// Import libraries (resolved against the pinned Windows SDK x64 lib tree) read to
    /// recover the faithful function → DLL mapping the headers do not carry. Ordered
    /// first-wins: the per-DLL host libraries (`kernel32.lib` → `KERNEL32.dll`) that match
    /// the classic Win32 desktop surface come first, so every classic export keeps its
    /// host DLL; the trailing `api-ms-win-*` umbrella libraries (`onecore.lib`,
    /// `onecoreuap.lib`) then stamp the modern apiset-only residue (`CreateFileMapping2`,
    /// the `*FromApp` file APIs, …) with the faithful apiset contract name, matching the
    /// reference metadata, which likewise leaves no function with an empty library.
    import_libs: Vec<String>,
    /// In-scope header directory segments (e.g. `["um", "shared"]`). A declaration whose
    /// defining header lives under one of these SDK directories is emitted
    /// unconditionally; anything else the `windows.h` prelude transitively pulls in (the
    /// C-runtime under `ucrt`, the MSVC toolset `include`) is emitted only when an
    /// in-scope declaration references it, and is otherwise dropped. This is
    /// reachability-by-reference capture: it scopes the metadata to the Windows API
    /// surface without an allowlist (the genuine cross-over types — `size_t`, `va_list`,
    /// `EXCEPTION_DISPOSITION` — survive because in-scope APIs reference them).
    #[serde(default)]
    scope: Vec<String>,
    /// Architectures to scrape and arch-merge. The committed RDL corpus is always
    /// x64-canonical; any additional arch listed here (`arm64`, `x86`) is scraped to a
    /// throwaway winmd and folded in via `SupportedArchitecture` so symbols that exist
    /// on only a subset of arches are tagged. Empty/`["x64"]` = single-arch (default).
    #[serde(default)]
    archs: Vec<String>,
}

fn main() {
    let time = std::time::Instant::now();

    let manifest_path = "crates/tools/win32/src/win32.toml";
    let manifest: Manifest = toml::from_str(
        &std::fs::read_to_string(manifest_path)
            .unwrap_or_else(|e| panic!("failed to read `{manifest_path}`: {e}")),
    )
    .unwrap_or_else(|e| panic!("failed to parse `{manifest_path}`: {e}"));
    assert_no_duplicate_headers(&manifest, manifest_path);
    for name in &manifest.archs {
        assert!(
            Arch::known(name).is_some(),
            "`{manifest_path}`: unknown architecture `{name}` in `archs` (known: x64, arm64, x86)"
        );
    }

    // Validate the toolchain is the pinned, reproducible one before scraping a single
    // header: the loaded libclang must match `LIBCLANG_VERSION`, and the pinned SDK
    // packages must be restored. This is what guarantees the committed corpus can be
    // regenerated byte-for-byte here and in CI. Both are provisioned on demand (the
    // pinned `libclang` wheel and SDK NuGet packages are fetched + cached on first use)
    // so a fresh checkout "just works" without a manual `pip install` / `nuget restore`.
    ensure_libclang();
    assert_libclang_version();
    let include_dirs = sdk_include_dirs();
    let lib_dirs = sdk_lib_dirs();

    // The faithful function → DLL mapping the headers don't carry, resolved to absolute
    // paths against the pinned SDK import libraries (per-DLL host libs first, `api-ms-win-*`
    // umbrella last).
    let import_libs: Vec<String> = manifest
        .import_libs
        .iter()
        .map(|lib| resolve(lib, &lib_dirs, "import library", "pinned SDK lib"))
        .collect();
    println!("Import libraries: {} entries", import_libs.len());

    // SDK include directories, passed to clang as `-isystem` so `#include <…>` resolves.
    let include_args: Vec<String> = include_dirs
        .iter()
        .flat_map(|dir| ["-isystem".to_string(), dir.clone()])
        .collect();

    // Build the main translation unit: the prelude (windows.h) followed by every API
    // header. This TU keeps `DEFINE_GUID` in its definition mode (an included header turns
    // `INITGUID` on), so wrapper-macro GUIDs whose values exist only in the expanded
    // initializer (`vfw.h`'s `DEFINE_AVIGUID`, …) are captured.
    let mut source = String::from(PRELUDE);
    for header in &manifest.headers {
        source.push_str(&format!("\n#include <{header}>"));
    }

    // Build a satellite translation unit for headers that cannot join the main TU because
    // they place `DEFINE_GUID` blocks outside their include guards (the device-driver
    // families). Parsed in isolation with `GUID_RESET` after every include, `INITGUID`
    // stays undefined so the repeated blocks are harmless declarations; every shared
    // declaration is deduplicated against the main TU by clang USR. Omitted when no
    // satellite headers are configured.
    let mut sources = vec![source];
    if !manifest.satellite_headers.is_empty() {
        let mut satellite = String::from(PRELUDE);
        satellite.push_str(GUID_RESET);
        for header in &manifest.satellite_headers {
            satellite.push_str(&format!("\n#include <{header}>"));
            satellite.push_str(GUID_RESET);
        }
        sources.push(satellite);
    }

    // x64 is always canonical (its scrape writes the committed corpus); any other arch the
    // manifest lists is folded in via arch-merge.
    let mut archs = vec![Arch::known("x64").unwrap()];
    for name in &manifest.archs {
        if name != "x64" {
            archs.push(Arch::known(name).unwrap());
        }
    }

    let scope_headers: Vec<String> = manifest
        .headers
        .iter()
        .chain(&manifest.satellite_headers)
        .cloned()
        .collect();

    let config = Config {
        root: manifest.root,
        rdl_dir: RDL_DIR.to_string(),
        out_dir: OUT_DIR.to_string(),
        winmd: WINMD.to_string(),
        archs,
        clang_args: CLANG_ARGS.iter().map(|s| s.to_string()).collect(),
        force_includes: vec![SAL_SHIM.to_string()],
        include_shim_dirs: vec![SHIM_DIR.to_string()],
        include_args,
        import_libs,
        drop_lib_less: true,
        scope: manifest.scope,
        scope_headers,
        sources,
        reference_winmds: Vec::new(),
        seed: Some(format!("{RDL_DIR}/{METADATA_SEED}")),
        parallel: true,
    };

    let summary = windows_scraper::run(&config);

    println!("Timing:");
    for (name, secs) in &summary.arch_timings {
        println!("  scrape {name:<6}         {secs:>8.2}s");
    }
    println!("  scrape (parallel wall) {:>8.2}s", summary.scrape_wall);
    if summary.multi_arch {
        println!(
            "Arch-merged: {}",
            config
                .archs
                .iter()
                .map(|a| a.name.as_str())
                .collect::<Vec<_>>()
                .join(" + ")
        );
        println!("  arch-merge             {:>8.2}s", summary.merge_wall);
        println!("  final winmd            {:>8.2}s", summary.winmd_wall);
    }
    println!("Wrote {WINMD} ({} partition(s))", summary.partitions);
    println!("Finished in {:.2}s", time.elapsed().as_secs_f32());
}

/// Panics if any header name appears more than once across the manifest's `headers` and
/// `satellite-headers` lists. Duplicates are silently absorbed by clang's USR dedup, so a
/// stray copy would otherwise go unnoticed; failing fast keeps the header lists honest.
fn assert_no_duplicate_headers(manifest: &Manifest, path: &str) {
    let mut seen = std::collections::BTreeSet::new();
    for header in manifest.headers.iter().chain(&manifest.satellite_headers) {
        assert!(
            seen.insert(header.as_str()),
            "duplicate header `{header}` in `{path}` (listed more than once across `headers` / `satellite-headers`)"
        );
    }
}

/// The pinned SDK include directories, in a fixed order so the parse is deterministic.
fn sdk_include_dirs() -> Vec<String> {
    let base = nuget_package("microsoft.windows.sdk.cpp", SDK_VERSION)
        .join("c")
        .join("Include")
        .join(SDK_INCLUDE_DIR);
    ["ucrt", "um", "shared", "winrt", "cppwinrt"]
        .iter()
        .map(|seg| base.join(seg).to_string_lossy().replace('\\', "/"))
        .collect()
}

/// The pinned SDK x64 import-library directories. The function → DLL mapping recorded by
/// an import lib is arch-invariant (the DLL that exports a symbol is the same on every
/// arch), so the x64 libs serve the canonical corpus and every additional arch scrape.
fn sdk_lib_dirs() -> Vec<String> {
    let base = nuget_package("microsoft.windows.sdk.cpp.x64", SDK_VERSION).join("c");
    ["um", "ucrt"]
        .iter()
        .map(|seg| {
            base.join(seg)
                .join("x64")
                .to_string_lossy()
                .replace('\\', "/")
        })
        .collect()
}

fn resolve(name: &str, dirs: &[String], kind: &str, var: &str) -> String {
    for dir in dirs {
        let candidate = Path::new(dir).join(name);
        if candidate.is_file() {
            return candidate.to_string_lossy().replace('\\', "/");
        }
    }
    panic!("{kind} `{name}` not found in any `{var}` directory");
}
