use windows_bindgen::*;

fn main() {
    let time = std::time::Instant::now();

    bindgen(["--etc", "crates/tools/bindings/src/collections.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/core.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/future_impl.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/future.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/metadata.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/numerics.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/registry.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/result.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/strings.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/version.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/threading.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/time.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/services.txt"]);

    bindgen(["--etc", "crates/tools/bindings/src/canvas.txt"]);
    bindgen(["--etc", "crates/tools/bindings/src/animation.txt"]);

    generate_webview_bindings();

    // NOTE: Reactor bindings are generated by tool_reactor.
    // Package crates (windows, windows-sys) are generated by tool_package.

    println!("Finished in {:.2}s", time.elapsed().as_secs_f32());
}

/// Compiles `WebView2.rdl` into a `.winmd` and then generates the minimal
/// `windows-webview` bindings from it.
fn generate_webview_bindings() {
    windows_rdl::reader()
        .input("crates/libs/webview/src/WebView2.rdl")
        .input("crates/libs/bindgen/default")
        .output("target/webview/WebView2.winmd")
        .write()
        .unwrap();

    bindgen(["--etc", "crates/tools/bindings/src/webview.txt"]);
}
