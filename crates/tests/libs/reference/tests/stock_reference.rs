#![cfg(windows)]

use windows_core::*;
use windows_reference::*;

#[test]
fn primitive_i32() -> Result<()> {
    let r = IReference::<i32>::from(42);
    assert_eq!(r.Value()?, 42);
    Ok(())
}

#[test]
fn primitive_bool() -> Result<()> {
    let r = IReference::<bool>::from(true);
    assert!(r.Value()?);

    let r = IReference::<bool>::from(false);
    assert!(!r.Value()?);
    Ok(())
}

#[test]
fn primitive_f64() -> Result<()> {
    let r = IReference::<f64>::from(1.5);
    assert_eq!(r.Value()?, 1.5);
    Ok(())
}

#[test]
fn hstring_value() -> Result<()> {
    let r = IReference::<HSTRING>::from(HSTRING::from("hello"));
    assert_eq!(r.Value()?, HSTRING::from("hello"));
    Ok(())
}

#[test]
fn guid_value() -> Result<()> {
    let g = GUID::from_u128(0x11223344_5566_7788_99AA_BBCCDDEEFF00);
    let r = IReference::<GUID>::from(g);
    assert_eq!(r.Value()?, g);
    Ok(())
}

#[test]
fn use_as_inspectable() -> Result<()> {
    // A boxed `IReference<T>` can be cast to `IInspectable` since `IReference`
    // derives from `IInspectable`.
    let r = IReference::<i32>::from(99);
    let _inspectable: IInspectable = r.cast()?;
    Ok(())
}

#[test]
fn cloning_preserves_value() -> Result<()> {
    let r = IReference::<i32>::from(7);
    let r2 = r.clone();
    assert_eq!(r.Value()?, 7);
    assert_eq!(r2.Value()?, 7);
    Ok(())
}

// ABI conformance: `IReference<T>` derives from `IPropertyValue`, so a boxed
// reference must respond to `QueryInterface(IID_IPropertyValue)` and dispatch
// the `IPropertyValue` vtable correctly. The stock implementation reports
// `PropertyType::OtherType` for `Type()`, returns `false` from
// `IsNumericScalar()`, and rejects typed accessors with `E_NOTIMPL`.
//
// The `IPropertyValue` projection used here is generated by `build.rs` (see
// `crates/tests/libs/reference/build.rs`) so the test exercises the stock
// implementation through bindgen-emitted safe wrappers rather than a
// hand-rolled vtable. This guards against subtle ABI drift in either the
// implementation or the projection.

use test_libs_reference::{IPropertyValue, PropertyType};

const E_NOTIMPL: HRESULT = HRESULT(0x80004001_u32 as _);

#[test]
fn property_value_abi() -> Result<()> {
    let r = IReference::<i32>::from(123);

    // QueryInterface for IPropertyValue must succeed: IReference<T> derives from it.
    let pv: IPropertyValue = r.cast()?;

    // `Type()` must report `OtherType` per the stock implementation contract.
    assert_eq!(pv.Type()?, PropertyType::OtherType);

    // `IsNumericScalar()` must report `false`.
    assert!(!pv.IsNumericScalar()?);

    // Typed accessors must return E_NOTIMPL — callers should use `Value()`.
    assert_eq!(pv.GetUInt8().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt32().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt32().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt64().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt64().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetSingle().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetDouble().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetChar16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetBoolean().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetString().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetGuid().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetDateTime().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetTimeSpan().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetPoint().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetSize().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetRect().unwrap_err().code(), E_NOTIMPL);

    Ok(())
}
