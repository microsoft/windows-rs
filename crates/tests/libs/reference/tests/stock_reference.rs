// ---------- Clone / equality ----------

#[test]
fn clone_preserves_value() -> Result<()> {
    let r = IReference::<i32>::from(7);
    let r2 = r.clone();
    assert_eq!(r.Value()?, 7);
    assert_eq!(r2.Value()?, 7);
    Ok(())
}

#[test]
fn equality() {
    let r1 = IReference::<i32>::from(10);
    let r2 = r1.clone();
    // Clones share the same COM identity.
    assert_eq!(r1, r2);
}

// ---------- IPropertyValue ABI conformance ----------
//
// `IReference<T>` derives from `IPropertyValue`. The stock implementation:
// - Reports the correct `PropertyType` for known types.
// - Reports `IsNumericScalar() == true` for integer/float types.
// - Returns the value from the matching typed accessor (e.g. `GetInt32` for i32).
// - Returns `E_NOTIMPL` for mismatched typed accessors.
//
// The `IPropertyValue` projection used here is generated by `build.rs` (see
// `crates/tests/libs/reference/build.rs`) so the tests exercise the stock
// implementation through bindgen-emitted safe wrappers rather than a
// hand-rolled vtable.

use test_libs_reference::{IPropertyValue, PropertyType, Vector2};

const E_NOTIMPL: HRESULT = HRESULT(0x80004001_u32 as _);

#[test]
fn property_type_i32() {
    let r = IReference::<i32>::from(123);
    let pv: IPropertyValue = r.cast().unwrap();
    assert_eq!(pv.Type().unwrap(), PropertyType::Int32);
    assert!(pv.IsNumericScalar().unwrap());
    assert_eq!(pv.GetInt32().unwrap(), 123);

    // Mismatched accessors return E_NOTIMPL.
    assert_eq!(pv.GetUInt8().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt32().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt64().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt64().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetSingle().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetDouble().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetBoolean().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetString().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetGuid().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetDateTime().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetTimeSpan().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetPoint().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetSize().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetRect().unwrap_err().code(), E_NOTIMPL);
}

