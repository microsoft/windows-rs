#[test]
fn property_type_other() {
    // Vector2 is a genuine WinRT struct (Windows.Foundation.Numerics.Vector2)
    // that is not in the PropertyType enum, so it exercises the OtherType path.
    let v = Vector2 { X: 1.0, Y: 2.0 };
    let r = IReference::<Vector2>::from(v);
    assert_eq!(r.Value().unwrap(), v);

    let pv: IPropertyValue = r.cast().unwrap();
    assert_eq!(pv.Type().unwrap(), PropertyType::OtherType);
    assert!(!pv.IsNumericScalar().unwrap());

    // All typed accessors return E_NOTIMPL for OtherType.
    assert_eq!(pv.GetUInt8().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt16().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt32().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt32().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetInt64().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetUInt64().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetSingle().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetDouble().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetBoolean().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetString().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetGuid().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetDateTime().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetTimeSpan().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetPoint().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetSize().unwrap_err().code(), E_NOTIMPL);
    assert_eq!(pv.GetRect().unwrap_err().code(), E_NOTIMPL);
}

