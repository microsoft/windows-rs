use std::path::Path;

fn rust_ident(name: &str) -> String {
    let ident = name.replace('-', "_");
    match ident.as_str() {
        "as" | "break" | "const" | "continue" | "crate" | "else" | "enum" | "extern" | "false"
        | "fn" | "for" | "if" | "impl" | "in" | "let" | "loop" | "match" | "mod" | "move"
        | "mut" | "pub" | "ref" | "return" | "self" | "Self" | "static" | "struct" | "super"
        | "trait" | "true" | "type" | "unsafe" | "use" | "where" | "while" | "async" | "await"
        | "dyn" => format!("r#{ident}"),
        _ => ident,
    }
}

fn main() {
    println!("cargo:rerun-if-changed=input");

    let out = Path::new("../../../../target/test_rdl");
    std::fs::create_dir_all(out).unwrap();

    let input_dir = Path::new("input");
    let mut tests = String::from("// @generated by build.rs\n");

    if input_dir.is_dir() {
        let mut entries: Vec<_> = std::fs::read_dir(input_dir)
            .unwrap()
            .filter_map(|e| e.ok())
            .map(|e| e.path())
            .filter(|p| p.extension().is_some_and(|ext| ext == "rdl"))
            .collect();
        entries.sort();

        for path in &entries {
            let stem = path.file_stem().unwrap().to_string_lossy();
            let ident = rust_ident(&stem);
            tests.push_str(&format!("#[test]\nfn {ident}() {{ run({stem:?}); }}\n"));
        }
    }

    std::fs::write(out.join("generated_tests.rs"), tests).unwrap();
}
