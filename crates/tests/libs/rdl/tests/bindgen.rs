/// Runs the windows-rdl reader on an inline RDL string, then runs windows-bindgen on the resulting
/// winmd, and returns the generated Rust source as a string.
///
/// `extra_args` are appended after `--in <winmd> --out <rs> --filter Test`.
fn run_bindgen(rdl: &str, extra_args: &[&str]) -> String {
    let tmp = std::env::temp_dir();
    let winmd = tmp.join("test_rdl_bindgen.winmd");
    let out = tmp.join("test_rdl_bindgen.rs");
    let winmd_str = winmd.to_string_lossy().into_owned();
    let out_str = out.to_string_lossy().into_owned();

    windows_rdl::reader()
        .input_str(rdl)
        .output(&winmd_str)
        .write()
        .unwrap();

    let mut args = vec!["--in", &winmd_str, "--out", &out_str, "--filter", "Test"];
    args.extend_from_slice(extra_args);
    windows_bindgen::bindgen(args).unwrap();

    std::fs::read_to_string(&out).unwrap()
}

const SIMPLE_FN: &str = r#"
#[win32]
mod Test {
    #[library("test.dll")]
    extern fn Foo() -> u32;
}
"#;

/// Verifies that bindgen includes a "generated by" comment by default, and that `--no-comment`
/// suppresses it. This replaces the golden-file test `comment.rs` in `test_bindgen`, which used
/// the real `GetTickCount` API merely as a vehicle for this check.
#[test]
fn no_comment() {
    let with_comment = run_bindgen(SIMPLE_FN, &["--sys"]);
    assert!(
        with_comment.starts_with("// Bindings generated by"),
        "expected header comment but got:\n{with_comment}"
    );

    let without_comment = run_bindgen(SIMPLE_FN, &["--sys", "--no-comment"]);
    assert!(
        !without_comment.starts_with("//"),
        "expected no header comment but got:\n{without_comment}"
    );
}

/// Verifies that bindgen includes `#![allow(...)]` by default, and that `--no-allow` suppresses
/// it. This replaces the golden-file test `comment_no_allow.rs` in `test_bindgen`.
#[test]
fn no_allow() {
    let with_allow = run_bindgen(SIMPLE_FN, &["--sys"]);
    assert!(
        with_allow.contains("#![allow("),
        "expected #![allow] but got:\n{with_allow}"
    );

    let without_allow = run_bindgen(SIMPLE_FN, &["--sys", "--no-allow"]);
    assert!(
        !without_allow.contains("#![allow("),
        "expected no #![allow] but got:\n{without_allow}"
    );
}

/// Verifies that omitting `--in` defaults to the built-in Windows metadata, producing the same
/// output as `--in default`. This replaces the identical golden-file pair `default_default.rs`
/// and `default_assumed.rs` in `test_bindgen`.
#[test]
fn default_input() {
    let tmp = std::env::temp_dir();
    let explicit_path = tmp.join("test_rdl_bindgen_default_explicit.rs");
    let implicit_path = tmp.join("test_rdl_bindgen_default_implicit.rs");
    let explicit_str = explicit_path.to_string_lossy().into_owned();
    let implicit_str = implicit_path.to_string_lossy().into_owned();

    windows_bindgen::bindgen([
        "--in",
        "default",
        "--out",
        &explicit_str,
        "--filter",
        "GetTickCount",
        "--sys",
        "--flat",
        "--no-comment",
    ])
    .unwrap();

    windows_bindgen::bindgen([
        "--out",
        &implicit_str,
        "--filter",
        "GetTickCount",
        "--sys",
        "--flat",
        "--no-comment",
    ])
    .unwrap();

    assert_eq!(
        std::fs::read_to_string(&explicit_path).unwrap(),
        std::fs::read_to_string(&implicit_path).unwrap(),
    );
}
