use std::path::Path;

fn rust_ident(name: &str) -> String {
    let ident = name.replace('-', "_");
    match ident.as_str() {
        "as" | "break" | "const" | "continue" | "crate" | "else" | "enum" | "extern" | "false"
        | "fn" | "for" | "if" | "impl" | "in" | "let" | "loop" | "match" | "mod" | "move"
        | "mut" | "pub" | "ref" | "return" | "self" | "Self" | "static" | "struct" | "super"
        | "trait" | "true" | "type" | "unsafe" | "use" | "where" | "while" | "async" | "await"
        | "dyn" | "abstract" | "become" | "box" | "do" | "final" | "macro" | "override"
        | "priv" | "typeof" | "unsized" | "virtual" | "yield" | "try" => format!("r#{ident}"),
        _ => ident,
    }
}

fn main() {
    println!("cargo:rerun-if-changed=expected");

    let out = Path::new(&std::env::var("OUT_DIR").unwrap()).to_path_buf();
    std::fs::create_dir_all(&out).unwrap();

    // Compile-check the regenerated `expected/*.rs` bindings.
    let expected_dir = Path::new("expected");
    let manifest_dir = std::env::var("CARGO_MANIFEST_DIR")
        .unwrap()
        .replace('\\', "/");
    let mut modules = String::from("// @generated by build.rs\n");

    if expected_dir.is_dir() {
        let mut entries: Vec<_> = std::fs::read_dir(expected_dir)
            .unwrap()
            .filter_map(|e| e.ok())
            .map(|e| e.path())
            .filter(|p| p.extension().is_some_and(|ext| ext == "rs"))
            .collect();
        entries.sort();

        for path in entries {
            let stem = path.file_stem().unwrap().to_string_lossy().to_string();
            let ident = rust_ident(&stem);
            modules.push_str(&format!(
                "#[path = \"{manifest_dir}/expected/{stem}.rs\"]\nmod {ident};\n"
            ));
        }
    }

    std::fs::write(out.join("compile_fixtures.rs"), modules).unwrap();
}
